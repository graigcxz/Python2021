# 4-10 切片

names = ['yongjing', 'cuiyan', 'penglei', 'wuqin', 'maolin']

# 打印前三个元素
print('\nThe first three items in the list are:')
print(names[:3])

# 打印中间三个元素
print('\nThree items from the middle of the list are:')
print(names[1:4])


# 打印最后三个元素
print('\nThe last three items in the list are:')
print(names[-3:])